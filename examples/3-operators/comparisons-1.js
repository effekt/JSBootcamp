let x = 3;
let y = 5;
x == 3; //true
x == '3'; //true
x === '3'; //false
x === 3; //true
x == y; //false
x != y; //true
x !== 3; //false
x !== '3'; //true
y > x; //true
x <= 3; //true
x < 3; //false
